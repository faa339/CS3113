void Entity::AIChaser(Entity* player) {
    switch (aiState) {
    case IDLE:
        if (glm::distance(position, player->position) < 4.0f)
            aiState = WALKING;
        break;
    case WALKING:
        if(player->position.x < position.x)
            movement.x = -1;
        else
            movement.x = 1;
        break;
    }
}

void Entity::AI(Entity* player) {
    movement = glm::vec3(0);

    switch (aiType) {
    case CHASER:
        AIChaser(player);
        break;
    case JUMPER:
        AIJumper();
        break;
    }
}